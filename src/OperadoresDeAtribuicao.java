public class OperadoresDeAtribuicao {

    public static void main(String[] args) {
        
        System.out.println("=========================================================");
        int num = 5;
        num += 2;
        System.out.println("Resultado da adição de 5 com 2: " + num);
        num = 5;
        num -=2;
        System.out.println("Resultado da subtração de 5 pelo 2: " + num);
        num = 5;
        num *= 2;
        System.out.println("Resultado da multiplicação de 5 por 2: " + num);
        num = 5;
        num /= 2;
        System.out.println("Resultado da divisão de 5 por 2: " + num);
        num = 5;
        num %= 2;
        System.out.println("Resto da divisão de 5 por 2: " + num);
        System.out.println("Fim!");
        System.out.println("===================================================");
        System.out.println(5/2);
    }
    
}
