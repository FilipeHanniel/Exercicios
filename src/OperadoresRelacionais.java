public class OperadoresRelacionais {

    public static void main(String[] args) {
        
        System.out.println("==========================================");
        int primeiraVariavel = 1, segundaVariavel = 1;
        System.out.println("O valor da primeira variável é " + primeiraVariavel + "O valor da segunda é " + segundaVariavel) ;
        boolean primeiraVariavelEMenor = primeiraVariavel < segundaVariavel;
        System.out.println("Primeira é menor que a segunda? -> " + primeiraVariavel);
        boolean primeiraVariavelEMaiorIgual = primeiraVariavel >= segundaVariavel;
        System.out.println("Primeira é maior ou igual a segunda? -> " + primeiraVariavelEMaiorIgual);
        boolean primeiraVariavelEMenorIgual = primeiraVariavel <= segundaVariavel;
        System.out.println("Primeira é menor ou igual a segunra? -> " + primeiraVariavelEMenorIgual);

        System.out.println("Fim!");
        System.out.println("=================================================");
        

    }
    
}
